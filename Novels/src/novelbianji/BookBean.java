package novelbianji;

public class BookBean {
	private String novelname;
	private String novelwriter;
	private String noveltype;
	private String novelnumber;
	
	public String getNovelname() {
		return novelname;
	}
	public void setNovelname(String novelname) {
		this.novelname = novelname;
	}
	public String getNovelwriter() {
		return novelwriter;
	}
	public void setNovelwriter(String novelwriter) {
		this.novelwriter = novelwriter;
	}
	public String getNoveltype() {
		return noveltype;
	}
	public void setNoveltype(String noveltype) {
		this.noveltype = noveltype;
	}
	public String getNovelnumber() {
		return novelnumber;
	}
	public void setNovelnumber(String novelnumber) {
		this.novelnumber = novelnumber;
	}
}
